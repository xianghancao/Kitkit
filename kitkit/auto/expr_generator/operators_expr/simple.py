

# basic operator logic functions




def add(x, y):
	# (x + y)
	return '(%s+%s)' %(x, y)



def sub(x, y):
	# (x - y)
	return '(%s-%s)' %(x, y)



def mul(x, y):
	# (x * y)
	return '(%s*%s)' %(x, y)



def div(x, y):
	# (x/y)
	return '(%s/%s)' %(x, y)



def abve(x, y):
	# (x/y)
	return '(%s>%s)' %(x, y)


def below(x, y):
	# (x/y)
	return '(%s<%s)' %(x, y)


def equal(x, y):
	# (x/y)
	return '(%s==%s)' %(x, y)

